package day5;

public class RelationalOperator {

	public static void main(String[] args) {
		// Relational Operator: == equal, != not equal, < less than, > greater than, >=, <=
		int a = 4;
		int b = 5;
		System.out.println(a==b);//false
		System.out.println(a!=b);//true
		System.out.println(a>b);//false
		System.out.println(a<b);//true
		System.out.println(a<=b);//true
		System.out.println(a>=b);//false
	}

}
