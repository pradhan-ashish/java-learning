package day8;

public class WhileLoopDemo {
// for loop is used when there is certain number of times., while loop is used when it is uncertain
	public static void main(String[] args) {
		int i=1;// here inititlization is outside
		
	while (i<=5) {// condition is inside bracket
		i++;// increment is here
		System.out.println(i);
		
	}
	System.out.println("exit");
	}

}
